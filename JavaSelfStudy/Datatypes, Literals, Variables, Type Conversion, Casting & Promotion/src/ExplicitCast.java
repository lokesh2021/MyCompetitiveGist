public class ExplicitCast {
    public static void main(String[] args) {
        int c = 123;
        byte a = (byte) c;
        System.out.println(a); //Explicit TypeCasting: Converts higher level data type to lower level data type

        int y = 130;
        byte z = (byte)y;
        System.out.println(z);


    }
}
